'use client';

import React, { useRef, useState } from 'react';
import { motion, useInView } from 'framer-motion';
import { useLanguage } from '@/lib/i18n/LanguageProvider';
import './Contact.css';

const Contact: React.FC = () => {
  const containerRef = useRef<HTMLDivElement | null>(null);
  const isInView = useInView(containerRef, { once: false, margin: "-100px" });
  const { t } = useLanguage();
  const [submitted, setSubmitted] = useState(false);
  const [isAnimating, setIsAnimating] = useState(false);
  const [brief, setBrief] = useState<any>(null);
  const [aiLoading, setAiLoading] = useState(false);
  const [aiError, setAiError] = useState(false);

  const handleSubmit = async (e: React.FormEvent<HTMLFormElement>) => {
    e.preventDefault();
    setIsAnimating(true);
    setAiLoading(true);
    setAiError(false);
    setBrief(null);

    const formData = new FormData(e.target as HTMLFormElement);
    const name = formData.get('name');
    const email = formData.get('email');
    const message = formData.get('message');

    try {
      const res = await fetch('/api/brief', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ name, email, message })
      });
      const data = await res.json();
      if (data.brief) {
        setBrief(data.brief);
      } else {
        setAiError(true);
      }
    } catch (err) {
      setAiError(true);
    }
    setAiLoading(false);
    setSubmitted(true);
    setIsAnimating(false);
    setTimeout(() => {
      setSubmitted(false);
      (e.target as HTMLFormElement).reset();
      setBrief(null);
      setAiError(false);
    }, 6000);
  };

  const contactMethods = [
    {
      icon: (
        <svg viewBox="0 0 24 24" fill="currentColor" width="24" height="24">
          <path d="M20 4H4c-1.1 0-2 .9-2 2v12c0 1.1.9 2 2 2h16c1.1 0 2-.9 2-2V6c0-1.1-.9-2-2-2zm0 4l-8 5-8-5V6l8 5 8-5v2z"/>
        </svg>
      ),
      label: t('contact.methods.email.label'),
      value: 'hello@meetmazhar.site',
      href: 'mailto:hello@meetmazhar.site'
    },
    {
      icon: (
        <svg viewBox="0 0 24 24" fill="currentColor" width="24" height="24">
          <path d="M17.472 14.382c-.297-.149-1.758-.867-2.03-.967-.273-.099-.471-.148-.67.15-.197.297-.767.966-.94 1.164-.173.199-.347.223-.644.075-.297-.15-1.255-.463-2.39-1.475-.883-.788-1.48-1.761-1.653-2.059-.173-.297-.018-.458.13-.606.134-.133.298-.347.446-.52.149-.174.198-.298.298-.497.099-.198.05-.371-.025-.52-.075-.149-.669-1.612-.916-2.207-.242-.579-.487-.5-.669-.51-.173-.008-.371-.01-.57-.01-.198 0-.52.074-.792.372-.272.297-1.04 1.016-1.04 2.479 0 1.462 1.065 2.875 1.213 3.074.149.198 2.096 3.2 5.077 4.487.709.306 1.262.489 1.694.625.712.227 1.36.195 1.871.118.571-.085 1.758-.719 2.006-1.413.248-.694.248-1.289.173-1.413-.074-.124-.272-.198-.57-.347m-5.421 7.403h-.004a9.87 9.87 0 01-5.031-1.378l-.361-.214-3.741.982.998-3.648-.235-.374a9.86 9.86 0 01-1.51-5.26c.001-5.45 4.436-9.884 9.888-9.884 2.64 0 5.122 1.03 6.988 2.898a9.825 9.825 0 012.893 6.994c-.003 5.45-4.437 9.884-9.885 9.884m8.413-18.297A11.815 11.815 0 0012.05 0C5.495 0 .16 5.335.157 11.892c0 2.096.547 4.142 1.588 5.945L.057 24l6.305-1.654a11.882 11.882 0 005.683 1.448h.005c6.554 0 11.89-5.335 11.893-11.893a11.821 11.821 0 00-3.48-8.413z"/>
        </svg>
      ),
      label: t('contact.methods.whatsapp.label'),
      value: '+971 50 721 7156',
      href: 'https://wa.me/971507217156?text=Hi%20Mazhar%2C%20I%27d%20like%20to%20talk%20about%20a%20project.'
    },
    {
      icon: (
        <svg viewBox="0 0 24 24" fill="currentColor" width="24" height="24">
          <path d="M6.62 10.79c1.44 2.83 3.76 5.14 6.59 6.59l2.2-2.2c.27-.27.67-.36 1.02-.24 1.12.37 2.33.57 3.57.57.55 0 1 .45 1 1V20c0 .55-.45 1-1 1-9.39 0-17-7.61-17-17 0-.55.45-1 1-1h3.5c.55 0 1 .45 1 1 0 1.25.2 2.45.57 3.57.11.35.03.74-.25 1.02l-2.2 2.2z"/>
        </svg>
      ),
      label: t('contact.methods.phone.label'),
      value: '+971 50 721 7156',
      href: 'tel:+971507217156'
    }
  ];

  return (
    <section className="fold contact" ref={containerRef}>
      <div className="container">
        <motion.div
          className="contact-header"
          initial={{ opacity: 0, y: 40 }}
          animate={isInView ? { opacity: 1, y: 0 } : { opacity: 0, y: 40 }}
          transition={{ duration: 0.7 }}
        >
          <motion.span
            className="section-label"
            initial={{ opacity: 0 }}
            animate={isInView ? { opacity: 1 } : { opacity: 0 }}
            transition={{ delay: 0.1 }}
          >
            {t('contact.label')}
          </motion.span>
          
          <motion.h1
            initial={{ opacity: 0, y: 20 }}
            animate={isInView ? { opacity: 1, y: 0 } : { opacity: 0, y: 20 }}
            transition={{ delay: 0.2, duration: 0.7 }}
          >
            {t('contact.title')}
          </motion.h1>
          
          <motion.p
            className="contact-subtitle"
            initial={{ opacity: 0 }}
            animate={isInView ? { opacity: 1 } : { opacity: 0 }}
            transition={{ delay: 0.3, duration: 0.6 }}
          >
            {t('contact.subtitle')}
          </motion.p>
        </motion.div>

        <div className="contact-content">
          {/* Contact Form */}
          <motion.div
            className="contact-form-wrapper"
            initial={{ opacity: 0, x: -40 }}
            animate={isInView ? { opacity: 1, x: 0 } : { opacity: 0, x: -40 }}
            transition={{ delay: 0.4, duration: 0.6 }}
          >
            <form onSubmit={handleSubmit} className="contact-form">
              <div className="form-group">
                <label htmlFor="name">{t('contact.form.name')}</label>
                <input 
                  type="text" 
                  id="name" 
                  name="name"
                  placeholder={t('contact.form.namePlaceholder')}
                  required
                  disabled={isAnimating || submitted}
                />
              </div>

              <div className="form-group">
                <label htmlFor="email">{t('contact.form.email')}</label>
                <input 
                  type="email" 
                  id="email" 
                  name="email"
                  placeholder={t('contact.form.emailPlaceholder')}
                  required
                  disabled={isAnimating || submitted}
                />
              </div>

              <div className="form-group">
                <label htmlFor="subject">{t('contact.form.subject')}</label>
                <input 
                  type="text" 
                  id="subject" 
                  name="subject"
                  placeholder={t('contact.form.subjectPlaceholder')}
                  disabled={isAnimating || submitted}
                />
              </div>

              <div className="form-group">
                <label htmlFor="message">{t('contact.form.message')}</label>
                <textarea 
                  id="message" 
                  name="message"
                  placeholder={t('contact.form.messagePlaceholder')}
                  rows={5}
                  required
                  disabled={isAnimating || submitted}
                />
              </div>

              <motion.button
                type="submit"
                className="btn btn-primary contact-submit"
                disabled={isAnimating || submitted}
                whileHover={{ scale: submitted ? 1 : 1.05, y: submitted ? 0 : -4 }}
                whileTap={{ scale: submitted ? 1 : 0.95 }}
              >
                {submitted ? (
                  <motion.span
                    initial={{ opacity: 0 }}
                    animate={{ opacity: 1 }}
                    transition={{ duration: 0.3 }}
                  >
                    {t('contact.form.success')}
                  </motion.span>
                ) : isAnimating ? (
                  <motion.span
                    animate={{ rotate: 360 }}
                    transition={{ duration: 1, repeat: Infinity }}
                  >
                    ✈️
                  </motion.span>
                ) : (
                  <span>{t('contact.form.submit')}</span>
                )}
              </motion.button>

            </form>
          </motion.div>

          {/* Smart Brief Helper AI Result */}
          {(aiLoading || brief || aiError) && (
            <motion.div
              className="ai-brief-box"
              initial={{ opacity: 0, y: 20 }}
              animate={{ opacity: 1, y: 0 }}
              transition={{ duration: 0.5 }}
              style={{ marginTop: 32 }}
            >
              {aiLoading && (
                <span style={{ fontSize: '1.1rem', color: '#6366f1' }}>Thinking…</span>
              )}
              {brief && (
                <div>
                  <strong>Here’s what I understood from your message:</strong>
                  <ul style={{ marginTop: 12, marginBottom: 0 }}>
                    {brief.projectType && <li><b>Project type:</b> {brief.projectType}</li>}
                    {brief.deliverables && Array.isArray(brief.deliverables) && (
                      <li><b>Deliverables:</b>
                        <ul>
                          {brief.deliverables.map((d: string, i: number) => <li key={i}>{d}</li>)}
                        </ul>
                      </li>
                    )}
                    {brief.goals && <li><b>Goals:</b> {brief.goals}</li>}
                    {brief.urgency && <li><b>Urgency:</b> {brief.urgency}</li>}
                    {brief.notes && <li><b>Notes:</b> {brief.notes}</li>}
                  </ul>
                </div>
              )}
              {aiError && (
                <span style={{ color: '#f59e0b' }}>Couldn’t generate a brief, but your message was sent!</span>
              )}
            </motion.div>
          )}

          {/* Contact Methods */}
          <motion.div
            className="contact-methods"
            initial={{ opacity: 0, x: 40 }}
            animate={isInView ? { opacity: 1, x: 0 } : { opacity: 0, x: 40 }}
            transition={{ delay: 0.5, duration: 0.6 }}
          >
            <div className="methods-header">
              <h3>{t('contact.quickContact.title')}</h3>
              <p>{t('contact.quickContact.subtitle')}</p>
            </div>

            <div className="methods-list">
              {contactMethods.map((method: any, idx: number) => (
                <motion.a
                  key={idx}
                  href={method.href}
                  className="contact-method-card"
                  target={method.label === 'WhatsApp' ? '_blank' : undefined}
                  rel={method.label === 'WhatsApp' ? 'noopener noreferrer' : undefined}
                  initial={{ opacity: 0, y: 20 }}
                  animate={isInView ? { opacity: 1, y: 0 } : { opacity: 0, y: 20 }}
                  transition={{ delay: 0.6 + idx * 0.1 }}
                  whileHover={{
                    y: -8,
                    boxShadow: '0 12px 32px rgba(0, 0, 0, 0.15)',
                    transition: { duration: 0.3 }
                  }}
                >
                  <motion.span 
                    className="method-icon"
                    whileHover={{ scale: 1.2, rotate: 10 }}
                  >
                    {method.icon}
                  </motion.span>
                  <div className="method-content">
                    <span className="method-label">{method.label}</span>
                    <span className="method-value">{method.value}</span>
                  </div>
                  <motion.span 
                    className="method-arrow"
                    initial={{ x: 0 }}
                    whileHover={{ x: 6 }}
                  >
                    →
                  </motion.span>
                </motion.a>
              ))}
            </div>

            <motion.div
              className="contact-info"
              initial={{ opacity: 0 }}
              animate={isInView ? { opacity: 1 } : { opacity: 0 }}
              transition={{ delay: 0.95 }}
            >
              <p>{t('contact.info.location')}</p>
              <p>{t('contact.info.availability')}</p>
              <p>{t('contact.info.responseTime')}</p>
            </motion.div>
          </motion.div>
        </div>
      </div>
    </section>
  );
};

export default Contact;
